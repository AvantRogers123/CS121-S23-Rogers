package Activity24;

public class BigO {
    public static void main(String[] args) {
        public class BigO {
            public static void printOnce(Object obj) {
                System.out.println(obj);
            }

            public static void printNTimes(int n) {
                for (int i = 0; i < n; i++) {
                    System.out.println("Hello");
                }
            }

            public static void printNSquaredTimes(int n) {
                for (int i = 0; i < n; i++) {
                    for (int j = 0; j < n; j++) {
                        System.out.println("Hello");
                    }
                }
            }
        }

    }
}
